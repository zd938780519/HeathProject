package com.yidao.project.heathproject.Beans;

public class FTJBean {

    /**
     * code : 200
     * msg : SUCCESS
     * data : 心肺机能良好，无需刻意锻炼来提高，但要还继续保持。
     */

    private int code;
    private String msg;
    private String data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
